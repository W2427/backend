public class DwgListEntity {

    private String div;

    private String dwgDesc;

    private String dwgId;

    private String dwgNo;

    private String projNo;

    public String getDiv() {
        return div;
    }

    public void setDiv(String div) {
        this.div = div;
    }

    public String getDwgDesc() {
        return dwgDesc;
    }

    public void setDwgDesc(String dwgDesc) {
        this.dwgDesc = dwgDesc;
    }

    public String getDwgId() {
        return dwgId;
    }

    public void setDwgId(String dwgId) {
        this.dwgId = dwgId;
    }

    public String getDwgNo() {
        return dwgNo;
    }

    public void setDwgNo(String dwgNo) {
        this.dwgNo = dwgNo;
    }

    public String getProjNo() {
        return projNo;
    }

    public void setProjNo(String projNo) {
        this.projNo = projNo;
    }
}
