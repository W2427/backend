package com.ose.tasks.util;

public class PageInfo {

    private Float pw;

    private Float ph;

    private int pageRotation;

    public Float getPw() {
        return pw;
    }

    public void setPw(Float pw) {
        this.pw = pw;
    }

    public Float getPh() {
        return ph;
    }

    public void setPh(Float ph) {
        this.ph = ph;
    }

    public int getPageRotation() {
        return pageRotation;
    }

    public void setPageRotation(int pageRotation) {
        this.pageRotation = pageRotation;
    }
}
